package com.FlightAirport;

public class Flight {

	private Integer fid;
	private Integer fno;
	private String fname;
	private Airport obj;
	public Integer getFid() {
		return fid;
	}
	public void setFid(Integer fid) {
		this.fid = fid;
	}
	public Integer getFno() {
		return fno;
	}
	public void setFno(Integer fno) {
		this.fno = fno;
	}
	public String getFname() {
		return fname;
	}
	public void setFname(String fname) {
		this.fname = fname;
	}
	public Airport getObj() {
		return obj;
	}
	public void setObj(Airport obj) {
		this.obj = obj;
	}
	
}
