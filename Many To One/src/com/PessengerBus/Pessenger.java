package com.PessengerBus;

public class Pessenger {
private Integer pid;
private String pname;
private String pdest;
private Bus obj;
public Integer getPid() {
	return pid;
}
public void setPid(Integer pid) {
	this.pid = pid;
}
public String getPname() {
	return pname;
}
public void setPname(String pname) {
	this.pname = pname;
}
public String getPdest() {
	return pdest;
}
public void setPdest(String pdest) {
	this.pdest = pdest;
}
public Bus getObj() {
	return obj;
}
public void setObj(Bus obj) {
	this.obj = obj;
}

}
