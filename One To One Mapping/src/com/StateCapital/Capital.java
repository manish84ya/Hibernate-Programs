package com.StateCapital;

public class Capital {

	private Integer cid;
	private String cname;
	private State st;
	public Integer getCid() {
		return cid;
	}
	public void setCid(Integer cid) {
		this.cid = cid;
	}
	public String getCname() {
		return cname;
	}
	public void setCname(String cname) {
		this.cname = cname;
	}
	public State getSt() {
		return st;
	}
	public void setSt(State st) {
		this.st = st;
	}
	
}
