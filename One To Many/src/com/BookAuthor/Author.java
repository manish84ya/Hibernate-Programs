package com.BookAuthor;

public class Author 
{
	private Integer Aid;
	private String Aname;
	private Integer Amob;
	private Integer fid;
	public Integer getAid() {
		return Aid;
	}
	public void setAid(Integer aid) {
		Aid = aid;
	}
	public String getAname() {
		return Aname;
	}
	public void setAname(String aname) {
		Aname = aname;
	}
	public Integer getAmob() {
		return Amob;
	}
	public void setAmob(Integer amob) {
		Amob = amob;
	}
	public Integer getFid() {
		return fid;
	}
	public void setFid(Integer fid) {
		this.fid = fid;
}
	
}
