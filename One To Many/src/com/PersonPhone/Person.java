package com.PersonPhone;

import java.util.Set;

public class Person {
 private Integer id;
 private String name;
 private Set Child;
public Integer getId() {
	return id;
}
public void setId(Integer id) {
	this.id = id;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public Set getChild() {
	return Child;
}
public void setChild(Set child) {
	Child = child;
}
 
 
 
}
