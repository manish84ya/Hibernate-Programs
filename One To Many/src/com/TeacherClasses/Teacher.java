package com.TeacherClasses;

import java.util.Set;

public class Teacher {
private Integer tid;
private String tname;
private String tmob;
private Set child;
public Integer getTid() {
	return tid;
}
public void setTid(Integer tid) {
	this.tid = tid;
}
public String getTname() {
	return tname;
}
public void setTname(String tname) {
	this.tname = tname;
}
public String getTmob() {
	return tmob;
}
public void setTmob(String tmob) {
	this.tmob = tmob;
}
public Set getChild() {
	return child;
}
public void setChild(Set child) {
	this.child = child;
}




}
